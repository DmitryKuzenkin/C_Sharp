void Picture(int[,] image)
{
    for (int i = 0; i < image.GetLength(0); i++)
    {
        for (int j = 0; j < image.GetLength(1); j++)
        {
            if (image[i, j] == 0) Console.Write($" ");
            else Console.Write($"+");
        }
         Console.WriteLine();
    }
   
}
void FillPicture(int row, int col)
{
    if ( pic[row,col] == 0 )
    {
        pic[row,col] = 1;
        FillPicture(row - 1, col);
        FillPicture(row , col - 1);
        FillPicture(row + 1 , col);
        FillPicture(row , col + 1);

    }
}

